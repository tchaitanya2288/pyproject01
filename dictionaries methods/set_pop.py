# 12	Remove Arbitrary Element from Set           =       a_set.pop()

# Syntax: a_set.pop()

# Example:

s = set([3,4,5,6])
print(s.pop())
print(s)

# Output :
# 1
# set([2, 3, 4, 5])

'''Removes and returns an arbitrary element from set a_set. 
Raises an error if there are no elements to remove.'''
